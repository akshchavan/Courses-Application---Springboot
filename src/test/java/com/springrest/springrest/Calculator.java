package com.springrest.springrest;

public class Calculator {
    //addition..
	public int doSum(int a, int b,int c) { 
		return a+b+c;
	}
	
	//multiplication..
	public int doProduct(int a,int b) {
		return a*b;
	}
	
	//boolean..
	public boolean compareTwoNum(int a,int b) {
		return a==b;
	}
	
}
